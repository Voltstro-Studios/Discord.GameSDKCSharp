namespace Discord.GameSDK.Activities
{
	/// <summary>
	/// What to reply with for a join request
	/// </summary>
	public enum ActivityJoinRequestReply
	{
		/// <summary>
		/// Deny the join request
		/// </summary>
		No,

		/// <summary>
		/// Allow the join request
		/// </summary>
		Yes,

		/// <summary>
		/// Ignore the join request
		/// </summary>
		Ignore
	}
}